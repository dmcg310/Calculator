const x = parseInt(prompt("X: "));
const y = parseInt(prompt("Y: "));

function add() {
  const total = x + y;
  console.log(total);
}

function subtract() {
  const total = x - y;
  console.log(total);
}

function multiply() {
  const total = x * y;
  console.log(total);
}

function divide() {
  const total = x / y;
  console.log(total);
}

add();
subtract();
multiply();
divide();
